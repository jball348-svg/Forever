#!/usr/bin/env node

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

// Parse command line arguments
const args = process.argv.slice(2);
let sinceFlag = null;
let formatJson = false;

for (let i = 0; i < args.length; i++) {
  if (args[i] === '--since' && i + 1 < args.length) {
    sinceFlag = args[i + 1];
    i++; // Skip next argument
  } else if (args[i] === '--format' && i + 1 < args.length && args[i + 1] === 'json') {
    formatJson = true;
    i++; // Skip next argument
  }
}

// Conventional commit types and their descriptions
const commitTypes = {
  feat: 'Features',
  fix: 'Bug Fixes', 
  perf: 'Performance Improvements',
  refactor: 'Refactoring',
  docs: 'Documentation',
  chore: 'Chores',
  test: 'Tests'
};

function parseGitCommits() {
  try {
    // Build git log command
    let gitCommand = 'git log --pretty=format:"%H|%s|%ad" --date=short';
    
    if (sinceFlag) {
      gitCommand += ` ${sinceFlag}..HEAD`;
    }
    
    const output = execSync(gitCommand, { encoding: 'utf8' });
    const commits = output.trim().split('\n').filter(line => line);
    
    return commits.map(line => {
      const [sha, message, date] = line.split('|');
      return {
        sha: sha.substring(0, 7), // Short SHA
        message: message,
        date: date
      };
    });
  } catch (error) {
    console.error('Error fetching git commits:', error.message);
    return [];
  }
}

function groupCommitsByType(commits) {
  const grouped = {};
  
  // Initialize groups
  Object.keys(commitTypes).forEach(type => {
    grouped[type] = [];
  });
  
  // Add "other" group for commits that don't match conventional types
  grouped.other = [];
  
  commits.forEach(commit => {
    const message = commit.message;
    let type = 'other';
    
    // Check if message starts with a conventional commit type
    const typeMatch = message.match(/^(\w+)(\(.+\))?:/);
    if (typeMatch && commitTypes[typeMatch[1]]) {
      type = typeMatch[1];
    }
    
    grouped[type].push(commit);
  });
  
  return grouped;
}

function generateMarkdownChangelog(groupedCommits) {
  let changelog = '# Changelog\n\n';
  changelog += 'This file is automatically generated. Do not edit manually.\n\n';
  
  Object.entries(commitTypes).forEach(([type, title]) => {
    const commits = groupedCommits[type];
    if (commits && commits.length > 0) {
      changelog += `## ${title}\n\n`;
      commits.forEach(commit => {
        changelog += `- ${commit.message} (${commit.sha}, ${commit.date})\n`;
      });
      changelog += '\n';
    }
  });
  
  // Add "Other" section if there are any commits
  const otherCommits = groupedCommits.other;
  if (otherCommits && otherCommits.length > 0) {
    changelog += '## Other Changes\n\n';
    otherCommits.forEach(commit => {
      changelog += `- ${commit.message} (${commit.sha}, ${commit.date})\n`;
    });
    changelog += '\n';
  }
  
  return changelog;
}

function generateJsonChangelog(groupedCommits) {
  const jsonOutput = {};
  
  Object.entries(commitTypes).forEach(([type, title]) => {
    const commits = groupedCommits[type];
    if (commits && commits.length > 0) {
      jsonOutput[type] = {
        title: title,
        commits: commits
      };
    }
  });
  
  // Add "other" section if there are any commits
  const otherCommits = groupedCommits.other;
  if (otherCommits && otherCommits.length > 0) {
    jsonOutput.other = {
      title: 'Other Changes',
      commits: otherCommits
    };
  }
  
  return jsonOutput;
}

function main() {
  console.log('Generating changelog...');
  
  const commits = parseGitCommits();
  console.log(`Found ${commits.length} commits`);
  
  if (commits.length === 0) {
    console.log('No commits found. Changelog will be empty.');
    return;
  }
  
  const groupedCommits = groupCommitsByType(commits);
  
  // Generate markdown changelog
  const markdownChangelog = generateMarkdownChangelog(groupedCommits);
  const outputPath = path.join(process.cwd(), 'CHANGELOG_AUTO.md');
  fs.writeFileSync(outputPath, markdownChangelog);
  console.log(`Markdown changelog written to: ${outputPath}`);
  
  // Generate JSON changelog if requested
  if (formatJson) {
    const jsonChangelog = generateJsonChangelog(groupedCommits);
    const jsonOutputPath = path.join(process.cwd(), 'scripts', 'changelog.json');
    fs.writeFileSync(jsonOutputPath, JSON.stringify(jsonChangelog, null, 2));
    console.log(`JSON changelog written to: ${jsonOutputPath}`);
  }
  
  console.log('Changelog generation complete!');
}

if (require.main === module) {
  main();
}

module.exports = {
  parseGitCommits,
  groupCommitsByType,
  generateMarkdownChangelog,
  generateJsonChangelog
};
